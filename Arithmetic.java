class Arithmetic {
    public static void main(String[]args) {
        int num = 100;
        int factor = 20;
        int sum = 0;
        sum = num + factor;
        System.out.println("addition result: " + sum);
        sum = num - factor;
        System.out.println("subtraction result: " + sum);
        sum = num * factor;
        System.out.println("increase result: " + sum);
        sum = num / factor;
        System.out.println("cleavage result: " + sum);

    }

}